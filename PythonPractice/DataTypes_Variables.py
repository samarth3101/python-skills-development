#Data Types

#Numeric (int, float, complex)
age = 20
pieValue = 3.14
complexValue = 3.14j

print(type(age))
print(age)
print(type(pieValue))
print(pieValue)
print(type(complexValue))
print(complexValue)

#Sequenced (List, Tuple)
myList = [1,2,3,4,5]
myTuple = (1,2,3,4,5)

print(type(myList))
print(myList)
print(type(myTuple))
print(myTuple)

#Mapped (dict)
mydict = {"Name" : "Samarth" , "Age" : 20}
print(type(mydict))
print(mydict)

#Set (set)
myset = {1,2,3,4,5,6,7,8,9}
print(type(myset))
print(myset)